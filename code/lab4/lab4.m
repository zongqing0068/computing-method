syms x;
g11(x) = cos(x)-x;
g12(x) = exp(-x)-sin(x);
g21(x) = x-exp(-x);
g22(x) = x*x-2*x*exp(-x)+exp(-2*x);
disp('问题1(1)的结果为:');
Newton(pi/4, 10.^(-6), 10.^(-4), 10, g11);
disp('问题1(2)的结果为:');
Newton(0.6, 10.^(-6), 10.^(-4), 10, g12);
disp('问题2(1)的结果为:');
Newton(0.5, 10.^(-6), 10.^(-4), 10, g21);
disp('问题2(2)的结果为:');
Newton(0.5, 10.^(-6), 10.^(-4), 20, g22);